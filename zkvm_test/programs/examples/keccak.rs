        Vec::from_hex("574271CD13959E8DDEAE5BFBDB02A3FDF54F2BABFD0CBEB893082A974957D0C1").unwrap();

    let mut hasher = Keccak::v256();
    let mut output = [0u8; 32];

    hasher.update(&input);
    hasher.finalize(&mut output);

    if output[..] != expected[..] {
        panic!("Keccak-256 hash mismatch");
    }

